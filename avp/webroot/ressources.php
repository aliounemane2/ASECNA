<p><b>La politique de gestion des ressources humaines s'insère dans la politique d'ensemble de l'ASECNA et contribue aux objectifs généraux de consolidation et de renforcement des acquis, de préparation des mutations à venir et de satisfaction des exigences de l'environnement</b></p>

		

L'Agence s'appuie fortement sur la compétence, la conscience élevée et l'engagement de son personnel autour des objectifs définis pour mener à bien sa mission.
Elle veille à la consolidation de relations professionnelles essentiellement animées par un souci partagé de donner à l'ASECNA de plus grandes chances de garantir sa compétitivité dans un contexte de plus en plus concurrentiel.<br/>

<p><b>Le dialogue social, un des piliers des progrès de l'ASECNA</b></p>

Ainsi, la sérénité du climat social contribue à l'optimisation de la gestion technique et financière de l'Agence, et créé les conditions idéales d'exploitation économique de l'ensemble des acteurs de la chaîne de transport aérien évoluant dans ses Etats membres.
D'où les efforts constants réalisés par l'ASECNA pour garantir les meilleures conditions de vie matérielles et de travail à ses agents.

Le dialogue social connaît un succès tel, que le Comité des Ministres a demandé le renouvellement du protocole d'accord unissant l'ASECNA à son personnel, et arrivé à échéance le 31 décembre 2011.
Ainsi, en application des Résolutions n° 2011 CA 120 – 7 du 07 juillet 2011, 2011 CA 120 – 21 du 09 juillet 2011 et 2011 CM 53 – 14 du 09 juillet 2011, un nouveau contrat d'entreprise a été signé le 4 décembre 2011, par le Directeur Général de l’ASECNA avec l’ensemble des Représentants des travailleurs. Prévu pour une durée de cinq (05) ans, ce nouvel accord, le 3ème du genre, couvre la période allant du 1er janvier 2012 au 31 décembre 2016. Il consacre l'engagement des deux parties à garantir un service de sécurité de la navigation aérienne dans les meilleures conditions, conformément à la réglementation en vigueur et aux attentes des usagers et des Etats membres. Il conforte, si besoin est, l’option courageuse, mais lucide, prise depuis 1999, de construire un partenariat social gagnant-gagnant, autour de principes et valeurs partagés et dont l’affermissement contribue significativement à la pérennité de l’Agence et à la prospérité de ses travailleurs.

<p>Ainsi, dans 18 Etats membres, ils sont plus de cinq mille personnes aux compétences diverses et complémentaires à travailler à la sécurité de millions de passagers et de centaines de milliers d'avions.
Par delà les frontières, ils sont unis dans cette grande communauté qui s'appelle l'ASECNA</p>

		
